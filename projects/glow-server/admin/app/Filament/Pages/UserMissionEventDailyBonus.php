<?php

namespace App\Filament\Pages;

use App\Constants\MissionStatus;
use App\Constants\UserSearchTabs;
use App\Filament\Pages\User\UserDataBasePage;
use App\Models\Mst\MstMissionEventDailyBonus;
use App\Models\Usr\UsrMissionEventDailyBonus;
use App\Tables\Columns\MstMissionDailyBonusInfoColumn;
use Filament\Forms\Components\TextInput;
use Filament\Tables\Actions\Action;
use Filament\Tables\Columns\TextColumn;
use Filament\Tables\Concerns\InteractsWithTable;
use Filament\Tables\Contracts\HasTable;
use Filament\Tables\Enums\ActionsPosition;
use Filament\Tables\Enums\FiltersLayout;
use Filament\Tables\Filters\Filter;
use Filament\Tables\Filters\SelectFilter;
use Filament\Tables\Table;
use Illuminate\Database\Eloquent\Builder;

class UserMissionEventDailyBonus extends UserDataBasePage implements HasTable
{
    use InteractsWithTable;

    protected static string $view = 'filament.pages.user-mission-daily-bonus';

    public string $currentTab = UserSearchTabs::MISSION_EVENT_DAILY_BONUS->value;

    public function mount()
    {
        parent::mount(); // TODO: Change the autogenerated stub
        $this->breadcrumbList = array_merge($this->breadcrumbList, [
            self::getUrl(['userId' => $this->userId]) => $this->currentTab,
        ]);
    }

    public function table(Table $table): Table
    {
        $query = MstMissionEventDailyBonus::query();

        $usrMissionEventDailyBonus = UsrMissionEventDailyBonus::query()
            ->where('usr_user_id', $this->userId)
            ->get()
            ->keyBy(function (UsrMissionEventDailyBonus $usrMissionEventDailyBonus) {
                return $usrMissionEventDailyBonus->mst_mission_event_daily_bonus_id;
            });

        return $table
            ->query($query)
            ->searchable(false)
            ->hiddenFilterIndicators()
            ->columns([
                MstMissionDailyBonusInfoColumn::make('mst_mission_daily_bonus_info')
                    ->label('ログボ情報')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionEventDailyBonus $mstMission) {
                            return $mstMission;
                        }
                    ),
                TextColumn::make('status_label')
                    ->label('ステータス')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionEventDailyBonus $mstMission) use ($usrMissionEventDailyBonus) {
                            $usr_mission_event_bonus = $usrMissionEventDailyBonus->get($mstMission->id);
                            return $usr_mission_event_bonus?->status_label ?? MissionStatus::LOCKED->label();
                        }
                    )
                    ->badge(true)
                    ->color(function (MstMissionEventDailyBonus $mstMission) use ($usrMissionEventDailyBonus) {
                        $usr_mission_event_bonus = $usrMissionEventDailyBonus->get($mstMission->id);
                        return $usr_mission_event_bonus?->status_badge ?? MissionStatus::LOCKED->badge();
                    }),
                TextColumn::make('cleared_at')
                    ->label('達成日時')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionEventDailyBonus $mstMission) use ($usrMissionEventDailyBonus) {
                            $usr_mission_event_bonus = $usrMissionEventDailyBonus->get($mstMission->id);
                            return $usr_mission_event_bonus?->cleared_at ?? '';
                        }
                    ),
                TextColumn::make('received_reward_at')
                    ->label('報酬受取日時')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionEventDailyBonus $mstMission) use ($usrMissionEventDailyBonus) {
                            $usr_mission_event_bonus = $usrMissionEventDailyBonus->get($mstMission->id);
                            return $usr_mission_event_bonus?->received_reward_at ?? '';
                        }
                    ),
                ])
            ->filters([
                Filter::make('id')
                    ->form([
                        TextInput::make('id')
                            ->label('ミッションID')
                    ])
                    ->label('ミッションID')
                    ->query(function (Builder $query, array $data): Builder {
                        if (blank($data['id'])) {
                            return $query;
                        }
                        return $query->where('id', $data['id']);
                    }),
                Filter::make('login_day_count')
                    ->form([
                        TextInput::make('login_day_count')
                            ->label('ログイン日数')
                    ])
                    ->label('ログイン日数')
                    ->query(function (Builder $query, array $data): Builder {
                        if (blank($data['login_day_count'])) {
                            return $query;
                        }
                        return $query->where('login_day_count', $data['login_day_count']);
                    }),
                SelectFilter::make('status')
                    ->options(MissionStatus::labels()->toArray())
                    ->query(function (Builder $query, $data): Builder {
                        if (blank($data['value'])) {
                            return $query;
                        }

                        if ($data['value'] == MissionStatus::LOCKED->value) {
                            $missionIds = UsrMissionEventDailyBonus::query()
                                ->where('usr_user_id', $this->userId)
                                ->pluck('mst_mission_event_daily_bonus_id');
                            return $query->whereNotIn('id', $missionIds);
                        }

                        $missionIds = UsrMissionEventDailyBonus::query()
                            ->where('usr_user_id', $this->userId)
                            ->where('status', $data)
                            ->pluck('mst_mission_event_daily_bonus_id');
                        return $query->whereIn('id', $missionIds);
                    })
                    ->label('ステータス'),
                ], FiltersLayout::AboveContent)
            ->deferFilters()
            ->filtersApplyAction(
                fn (Action $action) => $action
                    ->label('適用'),
            )
            ->actions([
                Action::make('edit')
                    ->label('編集')
                    ->button()
                    ->url(function (MstMissionEventDailyBonus $mstMission) {
                        return EditUserMissionEventDailyBonus::getUrl([
                            'userId' => $this->userId,
                            'mstMissionId' => $mstMission->id,
                        ]);
                    })
                    ->visible(fn () => EditUserMissionEventDailyBonus::canAccess()),
            ], position: ActionsPosition::BeforeColumns);
    }

}
