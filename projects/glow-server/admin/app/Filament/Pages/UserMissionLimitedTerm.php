<?php

namespace App\Filament\Pages;

use App\Constants\MissionStatus;
use App\Constants\UserSearchTabs;
use App\Constants\MissionLimitedTermCategory;
use App\Filament\Pages\User\UserDataBasePage;
use App\Tables\Columns\MstMissionInfoColumn;
use Filament\Forms\Components\TextInput;
use Filament\Tables\Actions\Action;
use Filament\Tables\Columns\TextColumn;
use Filament\Tables\Concerns\InteractsWithTable;
use Filament\Tables\Contracts\HasTable;
use Filament\Tables\Enums\ActionsPosition;
use Filament\Tables\Enums\FiltersLayout;
use Filament\Tables\Filters\Filter;
use Filament\Tables\Filters\SelectFilter;
use Filament\Tables\Table;
use Illuminate\Database\Eloquent\Builder;
use App\Models\Mst\MstMissionLimitedTerm;
use App\Models\Usr\UsrMissionLimitedTerm;
use App\Constants\MissionUnlockStatus;

class UserMissionLimitedTerm extends UserDataBasePage implements HasTable
{
    use InteractsWithTable;

    protected static string $view = 'filament.pages.user-mission-limited-term';

    public string $currentTab = UserSearchTabs::MISSION_LIMITED_TERM->value;

    public function mount()
    {
        parent::mount(); // TODO: Change the autogenerated stub
        $this->breadcrumbList = array_merge($this->breadcrumbList, [
            self::getUrl(['userId' => $this->userId]) => $this->currentTab,
        ]);
    }

    public function table(Table $table): Table
    {
        $query = MstMissionLimitedTerm::query()
            ->with([
                'mst_mission_i18n',
                'mst_mission_limited_term_dependency',
            ])
            ->orderby('criterion_count', 'asc');

        $usrMissionLimitedTerms = UsrMissionLimitedTerm::query()
            ->where('usr_user_id', $this->userId)
            ->get()
            ->keyBy(function (UsrMissionLimitedTerm $usrMissionLimitedTerm) {
                return $usrMissionLimitedTerm->mst_mission_limited_term_id;
            });

        return $table
            ->query($query)
            ->searchable(false)
            ->hiddenFilterIndicators()
            ->columns([
                MstMissionInfoColumn::make('mst_mission_info')
                    ->label('ミッション情報')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionLimitedTerm $mstMissionLimitedTerm) {
                            return $mstMissionLimitedTerm;
                        }
                    ),
                TextColumn::make('mission_category')
                    ->label('ミッションカテゴリ')
                    ->searchable(),
                TextColumn::make('status_label')
                    ->label('ステータス')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionLimitedTerm $mstMissionLimitedTerm) use ($usrMissionLimitedTerms) {
                            $usrMissionLimitedTerm = $usrMissionLimitedTerms->get($mstMissionLimitedTerm->id);
                            return $usrMissionLimitedTerm?->status_label ?? MissionStatus::LOCKED->label();
                        }
                    )
                    ->badge(true)
                    ->color(function (MstMissionLimitedTerm $mstMissionLimitedTerm) use ($usrMissionLimitedTerms) {
                        $usrMissionLimitedTerm = $usrMissionLimitedTerms->get($mstMissionLimitedTerm->id);
                        return $usrMissionLimitedTerm?->status_badge ?? MissionStatus::LOCKED->badge();
                    }),
                TextColumn::make('open_status')
                    ->label('開放ステータス')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionLimitedTerm $mstMissionLimitedTerm) use ($usrMissionLimitedTerms) {
                            $usrMissionEvent = $usrMissionLimitedTerms->get($mstMissionLimitedTerm->id);
                            return $usrMissionEvent?->is_open ? MissionUnlockStatus::OPEN->label() : MissionUnlockStatus::LOCK->label();
                        }
                    )
                    ->badge(true)
                    ->color(function (MstMissionLimitedTerm $mstMissionLimitedTerm) use ($usrMissionLimitedTerms) {
                        $usrMissionEvent = $usrMissionLimitedTerms->get($mstMissionLimitedTerm->id);
                        return $usrMissionEvent?->is_open ? MissionUnlockStatus::OPEN->badge() : MissionUnlockStatus::LOCK->badge();
                    }),
                TextColumn::make('progress')
                    ->label('進捗値')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionLimitedTerm $mstMissionLimitedTerm) use ($usrMissionLimitedTerms) {
                            $usrMissionLimitedTermProgress = $usrMissionLimitedTerms->get($mstMissionLimitedTerm->id);
                            return $usrMissionLimitedTermProgress?->progress ?? 0;
                        }
                    ),
                TextColumn::make('cleared_at')
                    ->label('達成日時')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionLimitedTerm $mstMissionLimitedTerm) use ($usrMissionLimitedTerms) {
                            $usrMissionLimitedTerm = $usrMissionLimitedTerms->get($mstMissionLimitedTerm->id);
                            return $usrMissionLimitedTerm?->cleared_at ?? '';
                        }
                    ),
                TextColumn::make('received_reward_at')
                    ->label('報酬受取日時')
                    ->searchable()
                    ->getStateUsing(
                        function (MstMissionLimitedTerm $mstMissionLimitedTerm) use ($usrMissionLimitedTerms) {
                            $usrMissionLimitedTerm = $usrMissionLimitedTerms->get($mstMissionLimitedTerm->id);
                            return $usrMissionLimitedTerm?->received_reward_at ?? '';
                        }
                    ),
            ])
            ->filters([
                Filter::make('id')
                    ->form([
                        TextInput::make('id')
                            ->label('ミッションID')
                    ])
                    ->label('ミッションID')
                    ->query(function (Builder $query, array $data): Builder {
                        if (blank($data['id'])) {
                            return $query;
                        }
                        return $query->where('id', $data['id']);
                    }),
                Filter::make('description')
                    ->form([
                        TextInput::make('description')
                            ->label('ミッション説明')
                    ])
                    ->label('ミッション説明')
                    ->query(function (Builder $query, array $data): Builder {
                        if (blank($data['description'])) {
                            return $query;
                        }
                        return $query->whereHas('mst_mission_i18n', function ($query) use ($data) {
                            $query->where('description', 'like', "%{$data['description']}%");
                        });
                    }),
                SelectFilter::make('mission_category')
                    ->options(MissionLimitedTermCategory::labels()->toArray())
                    ->query(function (Builder $query, $data): Builder {
                        if (blank($data['value'])) {
                            return $query;
                        }
                        return $query->where('mission_category', $data);
                    })
                    ->label('ミッションカテゴリ'),
                SelectFilter::make('status')
                    ->options(MissionStatus::labels()->toArray())
                    ->query(function (Builder $query, $data): Builder {
                        if (blank($data['value'])) {
                            return $query;
                        }

                        if ($data['value'] == MissionStatus::LOCKED->value) {
                            $missionIds = UsrMissionLimitedTerm::query()
                                ->where('usr_user_id', $this->userId)
                                ->pluck('id');
                            return $query->whereNotIn('id', $missionIds);
                        }

                        $missionIds = UsrMissionLimitedTerm::query()
                            ->where('usr_user_id', $this->userId)
                            ->where('status', $data)
                            ->pluck('id');
                        return $query->whereIn('id', $missionIds);

                    })
                    ->label('ステータス'),
                Filter::make('dependency_group_id')
                    ->form([
                        TextInput::make('dependency_group_id')
                            ->label('依存グループID')
                    ])
                    ->label('依存グループID')
                    ->query(function (Builder $query, array $data): Builder {
                        if (blank($data['dependency_group_id'])) {
                            return $query;
                        }
                        return $query->whereHas('mst_mission_limited_term_dependency', function ($query) use ($data) {
                            $query->where('group_id', $data['dependency_group_id'])
                                ->orderBy('unlock_order', 'asc');
                        });
                    }),
            ], FiltersLayout::AboveContent)
            ->deferFilters()
            ->filtersApplyAction(
                fn (Action $action) => $action
                    ->label('適用'),
            )
            ->actions([
                Action::make('edit')
                    ->label('編集')
                    ->button()
                    ->url(function (MstMissionLimitedTerm $mstMission) {
                        return EditUsrMissionLimitedTerm::getUrl([
                            'userId' => $this->userId,
                            'mstMissionId' => $mstMission->id,
                        ]);
                    })
                    ->visible(fn () => EditUsrMissionLimitedTerm::canAccess()),
            ], position: ActionsPosition::BeforeColumns);
    }
}
