-- log_exchange_actions
CREATE EXTERNAL TABLE `glow_prod_user_action_logs`.`log_exchange_actions` (
    `id` string COMMENT '',
    `usr_user_id` string COMMENT 'usr_users.id',
    `nginx_request_id` string COMMENT 'APIリクエスト単位でNginxにて生成されるユニークID',
